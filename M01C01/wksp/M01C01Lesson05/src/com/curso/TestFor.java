package com.curso;

public class TestFor {
    public static void main(String[] args) {        
        //Secciones del for
        int i = 0, j = 0;
        
        for( ; ; ) {
            if( i < 10 ) {
                System.out.printf("i = %d %n", i);
            } else {
                break;
            }
            ++i; --j;
        }
        
        
        
        
        
    }
}
